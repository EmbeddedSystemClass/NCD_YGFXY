void CheckCardStatues(void)
{
	
	if(CardPinIn)
		SetCardState(CardIN);
	else
		SetCardState(NoCard);

}

void UpDateGB_Time(void)
{
	MyTime_Def time ;
	
	RTC_GetTimeData(&time);

	SetGB_Time(&time);	
}

void ReadEnvironmentTemperature(void)
{
	float tempv = 0;
	
	tempv = DS18B20_Get_Temp();
	
	if(tempv > -200)
		SetGB_EnTemperature(tempv);
}
