void CheckCardStatues(void)
{
	CardState_Def temps = NoCard;
	
	if(CardPinIn)
		temps = CardIN;
	else
		temps = NoCard;
	
	if(temps != GetCardState())
	{
		SetCardState(temps);
		SendCardStatuesToQueue(&temps, 10*portTICK_RATE_MS);
	}
}


