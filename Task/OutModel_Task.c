#define OutModelTask_PRIORITY			2			//看门狗任务优先级
const char * OutModelTaskName = "vOutModelTask";		//看门狗任务名
/***************************************************************************************************/
/**************************************局部函数声明*************************************************/
/***************************************************************************************************/

