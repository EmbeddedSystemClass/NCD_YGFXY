/***************************************************************************************************
*FunctionName：StartvSysLedTask
*Description：建立系统指示灯任务
*Input：None
*Output：None
*Author：xsx
*Data：2015年8月26日16:58:13
***************************************************************************************************/
void StartvOutModelTask(void)
{
	OutModel_Init();
	
	xTaskCreate( vOutModelTask, OutModelTaskName, configMINIMAL_STACK_SIZE, NULL, OutModelTask_PRIORITY, NULL );
}

/***************************************************************************************************
*FunctionName：vSysLedTask
*Description：系统指示灯闪烁表面程序正常运行
*Input：None
*Output：None
*Author：xsx
*Data：2015年8月26日16:58:46
***************************************************************************************************/
static void vOutModelTask( void *pvParameters )
{
	while(1)
	{
		ChangeOutModelStatues();
		vTaskDelay(100 * portTICK_RATE_MS);	
	}
}
