void StartSystemStartTask(void)
{
	//显示更新程序界面
	SelectPage(0);
	IWDG_Feed();
	
	//检查是否有新程序
	if(My_Pass == checkNewAppFileIsExist())
	{
		while(1)
		{
			IWDG_Feed();
			delay_ms(100);
		}
	}
	else
		jumpToUserApplicationProgram();
}


